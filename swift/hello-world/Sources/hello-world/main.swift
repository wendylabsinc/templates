/// Wendy Lite — Hello World with HTTPS Request
///
/// Demonstrates making an HTTPS GET request from an ESP32-C6
/// using the WendyLite TLS APIs.

import WendyLite

// MARK: - Helpers

/// Print a static string to the Wendy console.
func print(_ message: StaticString) {
    message.withUTF8Buffer { buf in
        let ptr = UnsafeRawPointer(buf.baseAddress!).assumingMemoryBound(to: CChar.self)
        Console.print(ptr, length: Int32(buf.count))
    }
}

